enum class Language(val code: String) {
    En("en"),
    Es("es"),
    ;
    companion object {
        fun fromCode(code: String?): Language = when (code) {
            "es" -> Es
            else -> En
        }
    }
}

fun stringsFor(lang: Language): Strings = when (lang) {
    Language.En -> EN
    Language.Es -> ES
}

val LocalStrings = compositionLocalOf { EN }

val tr: Strings
    @Composable
    @ReadOnlyComposable
    get() = LocalStrings.current
