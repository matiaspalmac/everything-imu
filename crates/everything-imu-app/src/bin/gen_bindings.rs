//! Standalone binary that generates `apps/ui/src/api/bindings.ts` and
//! verifies idempotency. Skips the Tauri runtime so it works in CI.

fn main() {
    let out_dir = std::path::PathBuf::from("apps/ui/src/api");
    std::fs::create_dir_all(&out_dir).expect("create api dir");
    let target = out_dir.join("bindings.ts");
    let target_check = std::env::temp_dir().join("everything_imu_bindings_check.ts");

    let formatter = || {
        specta_typescript::Typescript::default()
            .bigint(specta_typescript::BigIntExportBehavior::Number)
            .header("// AUTO-GENERATED by tauri-specta. Do not edit.\n")
    };

    let builder = || everything_imu_app_lib::build_specta();

    builder().export(formatter(), &target).expect("export");
    builder()
        .export(formatter(), &target_check)
        .expect("export check");

    for path in [&target, &target_check] {
        everything_imu_app_lib::prepend_ts_nocheck(path.to_str().expect("utf8 path"));
    }

    let a = std::fs::read_to_string(&target).expect("read target");
    let b = std::fs::read_to_string(&target_check).expect("read check");
    if a != b {
        eprintln!("specta export NOT idempotent");
        std::process::exit(1);
    }

    println!("wrote {} ({} bytes)", target.display(), a.len());
}
